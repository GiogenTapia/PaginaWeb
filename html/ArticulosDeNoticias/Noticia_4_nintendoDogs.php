            <h2>Antiguo gerente general de la compañía recuerda como la franquicia superó las expectativas de Nintendo            </h2>
            <p>Dentro del gran catálogo de juegos exclusivos de Nintendo está Nintendogs. Esta franquicia fue desarrollada por Shigeru Miyamoto y además fue una de las que más impulsó el Nintendo DS y logró vender millones de unidades. Sin embargo, en un inicio había tantas dudas sobre el éxito de la propuesta a tal grado que varios miembros de Nintendo UK desestimaron la idea del juego.

                En una entrevista con GamesIndustry.biz, el antiguo gerente general de Nintendo UK, David Yarnton tuvo la oportunidad de hablar sobre como algunos empleados de la compañía recibieron el juego antes de probarlo y de que fuera un éxito.
                
                De acuerdo con el directivo, hubo algunos empleados, que también eran jugadores hardcore, que inicialmente despreciaron la idea sin darle siquiera una oportunidad, claramente influenciados por el estilo casual del juego. Sin embargo, el destino preparaba un giro interesante, pues luego esa indiferencia de los empleados se convirtió en amor por el juego.
                
                “Recuerdo haber visto un demo [de Nintendogs] en la oficina principal, muchos de los gamers hardcore en nuestro equipo lo menospreciaron. Tuve que reírme, porque semanas después, cuando recibimos ejemplares, podías escuchar personas hablando a sus cachorros en toda la oficinal — los hardcore fueron convertidos”, comentó Yarnton.</p>
            <h2>NINTENDOGS FUE UN JUEGO INSIGNIA QUE SUPERÓ LAS EXPECTATIVAS DE NINTENDO</h2>
            <p>Nintendogs no sólo rompió con los complejos de los empleados hardcore de Nintendo, sino que también propulsó las ventas de Nintendo DS gracias a que apelaba al mercado casual y superó por mucho las expectativas de la compañía. Yarnton, por el contrario, con orgullo menciona que siempre creyó en el juego.

                “Tenía expectativas tan grandes para Nintendogs y su potencial que cuando presenté nuestros pronósticos y pedidos en Japón, no me creyeron”, recuerda Yarnton, pero refiere que al final las ventas excedieron esas estimaciones “varias veces”.
                
                ¿Qué opinas de la respuesta por Nintendogs? ¿Probaste este juego en Nintendo DS o alguna entrega posterior de la serie? Cuéntanos en los comentarios.
                
                Nintendogs debutó en diversas versiones y tuvo varias ediciones posteriores con diferentes razas e incluso hubo algunos lanzamientos que incluyeron gatos, Nintendogs + Cats. Los juegos de la serie básicamente permitían al jugador interactuar con una mascota. Desafortunadamente, la última entrega de la serie llegó para Nintendo 3DS en 2011, y hasta el momento no hay planes para traerla de regreso.
                
                La única manera que ha estado vigente la serie es por medio de apariciones esporádicas en otros juegos de Nintendo, como en los de la serie Super Smash Bros. y en Super Mario Odyssey</p>
