            <h2>343i escuchará las peticiones de la comunidad para el futuro del juego</h2>
            <p>343 Industries ya confirmó que Halo: The Master Chief Collection tendrá mejoras para aprovechar todo el poder de Xbox Series X y Xbox Series S. Gracias a esto, los jugadores podrán disfrutar las campañas y el componente multijugador de la colección a 120 fps. Además, se añadirán mejoras para las partidas en pantalla dividida y opciones para ajustar el campo de visión. Un detalle que llamó la atención de los jugadores es que 343 Industries no mencionó nada sobre un posible soporte para ray tracing. Matt Hohl, productor de la serie, habló recientemente sobre las mejoras de nueva generación, incluyendo el trazado de rayos.</p>
            <h2>HALO: THE MASTER CHIEF COLLECTION CARECERÁ DE RAY TRACING EN XBOX SERIES X|S</h2>
            <p>Por medio de una publicación en Halo Waypoint (vía Pure Xbox), Matt Hohl reafirmó que, desde el 17 de noviembre, los usuarios de Xbox Series X y Xbox Series S podrán disfrutar una versión totalmente optimizada de Halo: The Master Chief Collection gracias a un update gratuito.

                El creativo puntualizó que los juegos podrán correr hasta a 120 fps y 4K en Xbox Series X, mientras que Xbox Series S los ejecutará a 1080p. Hohl señaló que tuvieron que superar varios desafíos técnicos para hacer esto posible.
                
                "Independientemente de que los jugadores tengan una pantalla para 120 fps, también hemos incluido algunas mejoras gráficas en el juego, como el aumento de las distancias de dibujado dentro de los niveles y mejoras en las experiencias de pantalla dividida en la campaña y el multijugador”, señaló Hohl.
                
                El creativo fue cuestionado sobre la posible implementación de ray tracing a la colección. Desafortunadamente, el Hohl no respondió la pregunta de manera directa, pues únicamente afirmó que está emocionado con lo que se puede lograr en las nuevas consolas.
                
                Así pues, Halo: The Master Chief Collection llegará a Xbox Series X y Xbox Series S sin trazado de rayos. Al menos por ahora, no hay planes para añadir dicha mejora a corto plazo. Sin embargo, las cosas podrían cambiar en el futuro.
                
                “La complejidad y el alcance de MCC plantean desafíos únicos de los que todavía estoy aprendiendo, pero me encantan las oportunidades que ofrece”, agregó el productor. Para finalizar, afirmó que escucharán todas las peticiones de la comunidad y las tomarán en cuenta.
                
                “Una cosa que ya he aprendido aquí es que este equipo escucha a la comunidad como un elemento crucial de nuestra planificación. Me encanta eso y estoy emocionado de ver lo que podemos hacer juntos en el futuro”, concluyó Hohl.</p>
